namespace ModulusFE.Indicators
{
  /// <summary>
  /// The Money Flow Index measures money flow of a security, using volume and price for calculations.
  /// </summary>
  /// <remarks>Market bottoms below 20 and tops above 80. Divergence of price and Money Flow Index are also used.
  /// <list type="table">
  /// <listheader>
  /// <term>Parameters</term>
  /// </listheader>
  /// <item><term>str Symbol</term></item>
  /// <item><term>str Volume</term></item>
  /// <item><term>int Periods</term></item>
  /// </list>
  /// </remarks>
  public class MoneyFlowIndex : Indicator
  {
    /// <summary>
    /// Constructor
    /// </summary>
    /// <param name="name">Indicator name</param>
    /// <param name="chartPanel">Reference to a panel where it will be placed</param>
    public MoneyFlowIndex(string name, ChartPanel chartPanel)
      : base(name, chartPanel)
    {
      _indicatorType = IndicatorType.MoneyFlowIndex;

      Init();
    }

    /// <summary>
    /// Action to be executd for calculating indicator
    /// </summary>
    /// <returns>for future usage. Must be ignored at this time.</returns>
    protected override bool TrueAction()
    {
// Validate
      int iSize = _chartPanel._chartX.RecordCount;
      if (iSize == 0)
        return false;

      int paramInt2 = ParamInt(2);
      if (paramInt2 < 2 || paramInt2 > iSize / 2)
      {
        ProcessError("Invalid Periods (min 2) for indicator " + FullName, IndicatorErrorType.ShowErrorMessage);
        return false;
      }


      // Get the data
      string paramStr0 = ParamStr(0);
      Field pOpen = SeriesToField("Open", paramStr0 + ".open", iSize);
      if (!EnsureField(pOpen, paramStr0 + ".open")) return false;
      Field pHigh = SeriesToField("High", paramStr0 + ".high", iSize);
      if (!EnsureField(pHigh, paramStr0 + ".high")) return false;
      Field pLow = SeriesToField("Low", paramStr0 + ".low", iSize);
      if (!EnsureField(pLow, paramStr0 + ".low")) return false;
      Field pClose = SeriesToField("Close", paramStr0 + ".close", iSize);
      if (!EnsureField(pClose, paramStr0 + ".close")) return false;
      string paramStr1 = ParamStr(1);
      Field pVolume = SeriesToField("Volume", paramStr1, iSize);
      if (!EnsureField(pVolume, paramStr1)) return false;

      Navigator pNav = new Navigator();
      Recordset pRS = new Recordset();

      pRS.AddField(pOpen);
      pRS.AddField(pHigh);
      pRS.AddField(pLow);
      pRS.AddField(pClose);
      pRS.AddField(pVolume);

      pNav.Recordset_ = pRS;


      // Calculate the indicator
      Index ta = new Index();
      Recordset pInd = ta.MoneyFlowIndex(pNav, pRS, paramInt2, FullName);


      // Output the indicator values
      Clear();
      for (int n = 0; n < iSize; ++n)
      {
        AppendValue(DM.TS(n), n < paramInt2 ? null : pInd.Value(FullName, n + 1));
      }

      return _calculateResult = PostCalculate();
    }
  }
}
